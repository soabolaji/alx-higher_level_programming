#!/usr/bin/python3
def add(a, b):
    """ Addition function

        Agrs:

        a: first integer
        b: second integer

        the return value is a + b
    """
    return (a + b)

def sub(a, b):
    """ Subtraction function

        Agrs:

        a: first integer
        b: second integer
                                                 the return value is a - b
    """
    return (a - b)

def mul(a, b):
    """ Multiplication function

        Agrs:

        a: first integer
        b: second integer
                                                 the return value is a * b
    """
    return (a * b)

def div(a, b):
    """ Division function

        Agrs:

        a: first integer
        b: second integer
                                                 the return value is a / b
    """
    return (a / b)
